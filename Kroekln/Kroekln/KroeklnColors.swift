import UIKit

extension UIColor {
   class func kroeklnBlue () -> UIColor {
      return UIColor(red: 26.0/225.0, green: 34.0/255.0, blue: 47.0/255.0, alpha: 1)
   }

   class func kroeklnDarkGreen() -> UIColor {
      return UIColor(red: 0/225.0, green: 144.0/255.0, blue: 177.0/255.0, alpha: 1)
   }

   class func kroeklnLightGreen() -> UIColor {
      return UIColor(red: 0/225.0, green: 144.0/255.0, blue: 177.0/255.0, alpha: 1)
   }

   class func kroeklnBeige() -> UIColor {
      return UIColor(red: 255.0/255.0, green: 235.0/255.0, blue: 173.0/255.0, alpha: 1)
   }

   class func kroeklnRed() -> UIColor {
      return UIColor(red: 215.0/255.0, green: 66.0/255.0, blue: 47.0/255.0, alpha: 1)
   }
}
